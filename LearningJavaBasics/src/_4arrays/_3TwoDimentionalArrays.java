package _4arrays;

public class _3TwoDimentionalArrays {

	public static void main(String[] args) {

      int[][] numbersInMathsAndScience = {{12,14}, {56,87}, {50,34}, {98,76}};
      
      
      
      System.out.println(numbersInMathsAndScience[2][1]);
      System.out.println(numbersInMathsAndScience[1][0]);
      
      String[][] nameAndGender  = {{"Jason", "M"}, {"Kerrie", "F"}, {"Ben", "M"}};
      System.out.println(nameAndGender[1][0]);
      
      Object[][] nameAndAge= {{"Jason",28 },{"Kerrie", 32},{"Lee", 49}};
      
      System.out.println(nameAndAge[2][0]);

	}

}
