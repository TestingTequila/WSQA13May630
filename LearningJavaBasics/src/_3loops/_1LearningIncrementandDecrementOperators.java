package _3loops;

public class _1LearningIncrementandDecrementOperators {

	public static void main(String[] args) {

          // Increment Operator => To increase the value of any numeric variable by 1]
		  //  variableName++;
		
		  // Decrement Operator => To decrease the value of any numeric variable by 1]
		  //  variableName--;
		
		  int num1= 99;
		  num1++;
		  
		  System.out.println(num1);
		  
		  
		  // Decrement Example
		  int num2= 99;
		  num2--;
		  
		  System.out.println(num2);

	}

}
