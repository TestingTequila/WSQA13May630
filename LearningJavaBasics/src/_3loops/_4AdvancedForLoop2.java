package _3loops;

public class _4AdvancedForLoop2 {

	public static void main(String[] args) {

		// WAP to print numbers from 1 -10;

		/*
		 * 
		 * for(int x= 1;x<11;x++) { System.out.println(x); }
		 * 
		 */

		// WAP to print numbers from 10 -1;

		/*
		 * 
		 * for (int y = 10;y > 0;y--) { System.out.println(y); }
		 * 
		 */

		// WAP to print table of 2

		for (int z = 1; z < 11; z++) 
		{
			System.out.println(2 * z);

		}

	}

}
